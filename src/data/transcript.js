const STORAGE_KEY = 'admin_transcript';

export function getTranscriptData(lang) {
  if (process.env.NODE_ENV === 'development') {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved) {
      try { return JSON.parse(saved)[lang] || transcriptData[lang]; } catch {}
    }
  }
  return transcriptData[lang];
}

export function saveTranscriptData(data) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
}

export function resetTranscriptData() {
  localStorage.removeItem(STORAGE_KEY);
}
